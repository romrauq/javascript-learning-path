// Declaring a variable (value is "undefined" by default):
let myVar1;
console.log(myVar1);

// Defining a variable (assigning a value to an undefined variable):
myVar1 = "This variable now has this string value assigned to it.";
console.log(myVar1);

// Initializing a variable (assigning a value as the variable is created):
let myVar2 = "I am the string initial value of this variable.";
console.log(myVar2);

// Reassigning/redefining a variable's value:
myVar2 = "I am the new string value of this variable.";
console.log(myVar2);
