num1 = 80;
num2 = 15;

if (num1 < num2) {
	console.log(`${num1} is less than ${num2}`); // This statement will run if the above statement is true.
} else {
	console.log(`${num1} is greater than ${num2}`); // This statement will run if the previous "if" statement is false.
}
// "80 is greater than 15" will be logged into the console area.
