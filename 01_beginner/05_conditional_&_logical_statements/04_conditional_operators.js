// Conditional Operators are used to compare variable values of the same datatype (<, >, <=, >=)
let myNum1 = 15;
let myNum2 = 23;

// "<" Checks if the value of the first argument is less than the second:
if (myNum1 < myNum2) {
	console.log(true);
} else {
	console.log(false);
}
// "<=" Checks if the value of the first argument is less than or equal to the second:
if (myNum1 <= 15) {
	console.log(true);
} else {
	console.log(false);
}
// Checks if the value of the first argument is greater than the second:
if (myNum1 > 20) {
	console.log(true);
} else {
	console.log(false);
}
// Checks if the value of the first argument is greater or equal to the second:
if (myNum2 >= 20) {
	console.log(true);
} else {
	console.log(false);
}
