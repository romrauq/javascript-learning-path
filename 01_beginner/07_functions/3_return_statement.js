// Return statement is used to get the value that a function evaluates.
// The return statement allows us to get the value of the evaluated function.

function myFunction(a, b) {
	// The "return" statement determines the value that this function will return.
	return a + b;
}

// Calling function with arguments:
console.log(myFunction(13, 6)); // The returned value of the function will be logged to the console.
