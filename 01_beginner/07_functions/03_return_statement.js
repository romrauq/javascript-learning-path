// A return statement is used to get the value that a function evaluates.

// Defining a function named "myFunction":
function myFunction(a, b) {
	// The "return" statement determines the value that this function will return.
	return a + b;
}

// Calling function with arguments:
console.log(myFunction(13, 6)); // The returned value of the function will be logged to the console.

// In the function execution case above, the function has to be run within the console log function in order to see its returned value. This is because the function simply returns the value and does not log it to any form of output.
