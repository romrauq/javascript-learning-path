// Function parameters allow functions to take in arguments and use those arguments to return a value. They are simply placeholders for arguments when the function is later called.

// Defining a function which has (two) parameters `a` & `b`:
function myFunction(a, b) {
	console.log(a + b);
}

// Executing the function with passed arguments:
myFunction(7, 6); // Function returns `13` in the console area.
